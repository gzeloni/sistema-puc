# A função trim_string recebe uma string como argumento 
# e retorna a mesma string com espaços em branco removidos do início e do final
# Utiliza o método strip() da classe str para realizar essa remoção
# O método strip() remove espaços em branco, 
# incluindo espaços, tabulações e quebras de linha, do início e do final da string
def trim_string(texto):
    return texto.strip()
